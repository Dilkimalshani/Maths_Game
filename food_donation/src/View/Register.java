    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String username=name.getText();
        String Email=email.getText();
        String Password=password.getText();
        String marks="0";
        Connection con1;
        PreparedStatement insert;
        if(username.equals("")){
            JOptionPane.showMessageDialog(null, "UserName c can't be empty ");
        }
        if(Email.equals("")){
            JOptionPane.showMessageDialog(null, "Email c can't be empty ");
        }
        if(Password.equals("")){
            JOptionPane.showMessageDialog(null, "Password c can't be empty ");
        }
        else{
            JOptionPane.showMessageDialog(null, " New player added");

            try {
                Class.forName("com.mysql.jdbc.Driver");
                con1 = DriverManager.getConnection("jdbc:mysql://localhost/game", "root", "");

                insert = con1.prepareStatement("insert into user(username,email,password,marks)values(?,?,?,?)");
                insert.setString(1, username);
                insert.setString(2,Email);
                insert.setString(3,Password);
                insert.setString(4,marks);

                insert.executeUpdate();
                //JOptionPane.showMessageDialog(this, " New player added");

            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null,e);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

